public class formet_specifier {
    public static void main(String[] args) {
        int num=10;
        double ff=2.83891112;
        char d='f';

        System.out.printf("Integer is = %d\n ",num);
        System.out.printf("Double value is =%.2f\n",ff);
        System.out.printf("Char is = %c\n",d);
    }
}
